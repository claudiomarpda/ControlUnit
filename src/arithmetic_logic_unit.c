#include "../include/arithmetic_logic_unit.h"

int add(int op1, int op2) {
    return op1 + op2;
}

int subtract(int op1, int op2) {
    return op1 - op2;
}

int multiply(int op1, int op2) {
    return op1 * op2;
}

int divide(int op1, int op2) {
    return op1 / op2;
}